#### use \t to tab
print("Python")
print("\tPython")

### use \n for a new line
print("Languages: \nPython\nJavascript\nC")

### you can combine both \t and \n
print("Languages: \n\tPython\n\tJavascript\n\tC")

### use rstrip() function to remove blank space
fav_lang='python '
print(fav_lang)
print(fav_lang.rstrip())



